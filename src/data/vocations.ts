/**
 * Returns all vocations available to a given class.
 * Includes class-specific vocations and free vocations.
 * For 'independiente', also includes merchant vocations (PDF p.47).
 */
export function getVocationsByClass(classId: ClassId): VocationDefinition[] {
  return VOCATIONS.filter((v) => {
    if (v.libre) return true
    if (v.clase.includes(classId)) return true
    if (classId === 'independiente' && v.clase.includes('mercader')) return true
    return false
  })
}
